package com.shadhinsoft.uapfall2018.Model;

public class CourseModel {

    private String couse_code, course_name, course_credit;

    public CourseModel() {

    }

    public String getCouse_code() {
        return couse_code;
    }

    public void setCouse_code(String couse_code) {
        this.couse_code = couse_code;
    }

    public String getCourse_name() {
        return course_name;
    }

    public void setCourse_name(String course_name) {
        this.course_name = course_name;
    }

    public String getCourse_credit() {
        return course_credit;
    }

    public void setCourse_credit(String course_credit) {
        this.course_credit = course_credit;
    }
}
